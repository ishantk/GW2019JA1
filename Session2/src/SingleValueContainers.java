
public class SingleValueContainers {

	public static void main(String[] args) {
		
		System.out.println("Program Started");
		
		// 1. Container Creation Statement
		int johnsAge = 20;
		// int is the type / class
		// johnsAge is name of storage container
		// = is to write data in container
		// 20 is data or literal
		
		int jenniesAge = 30;
		
		int jacksAge = 20;
		
		// 2. Container Updation Statement
		jacksAge = 30;
		
		// 3. Copy Operation
		int fionnasAge = jenniesAge;
		
		// 4. Read Storage Container
		System.out.println("Johns Age is:"+johnsAge);
		System.out.println(jenniesAge);
		System.out.println(jacksAge);
		System.out.println(fionnasAge);
		
		//System.out.println(); -> Prints in a new line
		//System.out.print(); -> Prints in same line
		
		// 5. Delete Operation
		// Happens Automatically : when main will be finished
		
		System.out.println("Program Finished");
	}

}
